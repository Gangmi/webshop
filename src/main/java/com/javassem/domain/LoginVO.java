package com.javassem.domain;

public class LoginVO {
	private String mid;
	private String mpass;
	private String mtel;
	private String memail;
	private String maddr;
	private String mgrade;
	private String msub;
	private String mpostCode;
	private String maddrDetail;
	
	
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getMpass() {
		return mpass;
	}
	public void setMpass(String mpass) {
		this.mpass = mpass;
	}
	public String getMtel() {
		return mtel;
	}
	public void setMtel(String mtel) {
		this.mtel = mtel;
	}
	public String getMemail() {
		return memail;
	}
	public void setMemail(String memail) {
		this.memail = memail;
	}
	public String getMaddr() {
		return maddr;
	}
	public void setMaddr(String maddr) {
		this.maddr = maddr;
	}
	public String getMgrade() {
		return mgrade;
	}
	public void setMgrade(String mgrade) {
		this.mgrade = mgrade;
	}
	public String getMsub() {
		return msub;
	}
	public void setMsub(String msub) {
		this.msub = msub;
	}
	public String getMpostCode() {
		return mpostCode;
	}
	public void setMpostCode(String mpostCode) {
		this.mpostCode = mpostCode;
	}
	public String getMaddrDetail() {
		return maddrDetail;
	}
	public void setMaddrDetail(String maddrDetail) {
		this.maddrDetail = maddrDetail;
	}
	
	
}
